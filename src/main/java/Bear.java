public class Bear {

    private String name;
    private int age;

    // constructor
    public Bear(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // getter method
    public String getName() {
        return this.name;
    }

    // setter method
    public void setName(String newName) {
        this.name = newName;
    }


    // getter method
    public int getAge() {
        return this.age;
    }

    // setter method
    public void setAge(int newAge) {
        this.age = newAge;
    }
}
